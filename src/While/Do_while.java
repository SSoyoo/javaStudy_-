package While;

public class Do_while {

	public static void main(String[] args) {
		/*
		 * do-while문
		 * "일단 한번은 실행하고(do)
		 * 조건이 참이면 명령을 반복해라! 
		 * 
		 * do{
		 * 		반복할 명령;
		 * }while(조건식); ture혹은 false의 결과식
		 * 
		 *
		 */
		
		int i = 50;
		do {
			System.out.println("적어도 한번은 실행되는 문장");
			
		}
		while(i<10);
		System.out.println("프로그램 종료");
		
		
		
	}

}
